public class D02math {
public static void main(String[] args) {
    int x = 5;
    int y = 9;

    System.out.println(x + y); // 14 (int)  -> "14"

    int z = x + y;
    System.out.println(z); // 14
    System.out.println("z" + z); // z=14

    double a = 0.2;
    double b = 0.4;

    double c = a - b;
    System.out.println(c); // -0.2

    int h = 40;
    int u = 3;

    int result = h * u;
    System.out.println(result); // 120

    


}

  
}
